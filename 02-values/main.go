package main 

import "fmt"

func main() {

	// String Concatenation
	fmt.Println("Clifford Opoku"   +  "Isaiah" ) // Clifford Opoku Isaiah


	// Integer Addition

	fmt.Println( 1 + 1 ) // 2

	// Float Addition

	fmt.Println( 1.1 + 1.1 ) // 2.2

	// Boolean

	fmt.Println( true && false ) // false

	// Boolean

	fmt.Println( true || false ) // true

	// Boolean

	fmt.Println( !true ) // false


	
}